# Python escape sequence demo

print("1. Backslash:        C:\\\\Users\\\\Name")
print("2. Single Quote:     It\'s a sunny day")
print("3. Double Quote:     She said: \"Hello!\"")
print("4. New Line:         Hello\nWorld")
print("5. Tab Space:        Hello\tWorld")
print("6. Backspace:        Hello\bWorld")  # Removes 'o'
print("7. Carriage Return:  Hello\rWorld")  # Overwrites "Hello" with "World"
print("8. Form Feed:        Hello\fWorld")  # Rarely visible, page break
print("9. Vertical Tab:     Hello\vWorld")  # Rarely visible
print("10. Bell Sound:      \a")            # Try with sound on
print("11. Octal Value:     \141")          # 'a'
print("12. Hex Value:       \x61")          # 'a'
